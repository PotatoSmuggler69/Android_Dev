package com.example.myapplication.Models;

public class State {
     String name;
    String capital;
    String info;
    String population;

    String area;

    int image_id;


    public State(String name, String capital, String info, String population, String area, int image_id) {
        this.name = name;
        this.capital = capital;
        this.info = info;
        this.population = population;
        this.area = area;
        this.image_id = image_id;
    }






}
